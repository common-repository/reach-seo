		<h5 class="panel-title">
			Broken Links
		</h5>
	</div>
	<div id="broken-links-variables" class="panel-collapse collapse" role="tabpanel" arialabelby="broken-links-variables-heading">
		<div class="panel-body">
			<ul class="list-unstyled">
				<li>
					<span class="label label-default">source.url</span>: The source of the broken link. In other words, where the link was found. Ex: 'http://www.example.com/awesome/page'
				</li>
				<li>
					<span class="label label-default">source.domain</span>: The source's domain. Ex: 'www.example.com'
				</li>
				<li>
					<span class="label label-default">source.metrics.da</span>: Domain authority of the source. Gaining a link from a higher domain authority is better for you and may ultimately increase your site's rankings. Ex: '35'
				</li>
				<li>
					<span class="label label-default">source.metrics.pa</span>: Page authority of the source. Same as <b>da</b> but specific to the url instead of entire domain. Ex: '45'
				</li>
				<li>
					<span class="label label-default">destination.url</span>: Where the broken link is pointing to. This is the resource that no longer exists. Ex: 'http://www.outdated.com/resource'
				</li>
				<li>
					<span class="label label-default">destination.domain</span>: Broken link's domain. Ex: 'www.outdated.com'
				</li>
			</ul>
		</div>
	</div>
</div>